// Numbers
let num = 133.45;
console.log(typeof num); // "number"

// Strings
let name = "Suhas";
console.log(typeof name); // "string"

// Booleans
let isStudent = true;
console.log(typeof isStudent); // "boolean"

// Undefined
let x;
console.log(typeof x); // "undefined"

// Null (special case)
let y = null;
console.log(typeof y); // "object" (weird JS bug)

