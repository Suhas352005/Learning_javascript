// Arithmetic Operators
console.log(10 % 4);       // 2
console.log(2 ** 4);       // 16

// Assignment Operators
let a = 10;
a += 5;
a *= 2;
console.log(a); // 30

// Comparison
console.log(5 == "5");   // true
console.log(5 === "5");  // false
console.log(10 > 5 && 3 < 1); // false

// Logical Mix
console.log(5 > 3 || 2 > 4 && 1 === 1); // true
